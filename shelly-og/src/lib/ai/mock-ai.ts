/** ניקוי וקיצור נושא מטקסט קלט. */
function topicOf(input: string): string {
  const clean = input.trim().replace(/\s+/g, " ");
  if (clean.length <= 90) return clean;
  return clean.slice(0, 87) + "…";
}

/** משפט פתיחה קצר מהנושא. */
function shortTopic(input: string): string {
  const t = topicOf(input);
  const firstClause = t.split(/[—:.,]/)[0].trim();
  return firstClause.length > 4 ? firstClause : t;
}

