/** ממיר פרופיל טון לטקסט הקשר עבור מנוע ה-AI. */
export function toneToContext(tone: ToneProfile): string {
  return [
    `הטון של Shelly: ${tone.summary}`,
    `תכונות הקול: ${tone.traits.join(", ")}.`,
    `כן לעשות: ${tone.dos.join("; ")}.`,
    `לא לעשות: ${tone.donts.join("; ")}.`,
    `ביטויים אופייניים: ${tone.signaturePhrases.join("; ")}.`,
    `קהל היעד: ${tone.audience}`,
    `אימוג'ים: ${tone.emoji}`,
    `סגירה אופיינית: ${tone.signoff}`,
  ].join("\n");
}
